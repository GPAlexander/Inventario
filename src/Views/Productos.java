    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Producto No Seleccionado");

        } else {
            int id = Integer.parseInt((String) jTable1.getValueAt(fila, 0).toString());
            String nombre = (String) jTable1.getValueAt(fila, 1);
            String descripcion = (String) jTable1.getValueAt(fila, 2);
            String stock = (String) jTable1.getValueAt(fila, 3);
            String precio = (String) jTable1.getValueAt(fila, 4);
            int id_proveedor = Integer.parseInt((String) jTable1.getValueAt(fila, 6).toString());

            jTextField3.setText("" + id);
            jTextField1.setText(nombre);
            jTextPane1.setText(descripcion);
            jTextField2.setText(stock);
            jTextField4.setText(precio);
            jComboBox1.setSelectedIndex(id_proveedor);

        }
    }//GEN-LAST:event_jTable1MouseClicked

