    private void listaTabla() {
        model.addColumn("Id");
        model.addColumn("Nombre");
        model.addColumn("Descripcion");
        model.addColumn("Stock");
        model.addColumn("precio");
        model.addColumn("Proveedor");
        model.addColumn("");
        jTable1.setModel(model);

    }

    private void GetList() {
        model.setRowCount(0);
        List<webservices.Productos> datos = pac.selectAllProductos();

        model = (DefaultTableModel) jTable1.getModel();

        Object[] ProductosObj = new Object[7];
        for (webservices.Productos u : datos) {
            ProductosObj[0] = u.getId();
            ProductosObj[1] = u.getNombre();
            ProductosObj[2] = u.getDescripcion();
            ProductosObj[3] = u.getStock();
            ProductosObj[4] = u.getPrecio();
            ProductosObj[5] = u.getProveedorNombre();
            ProductosObj[6] = u.getIdProveedor();
            model.addRow(ProductosObj);

        }
        jTable1.setModel(model);

    }

