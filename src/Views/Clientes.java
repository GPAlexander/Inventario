    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int fila=jTable1.getSelectedRow();
       if(fila==-1)
       {
           JOptionPane.showMessageDialog(null, "Usuario No Seleccionado");
           
       }else
       {
           int id=Integer.parseInt((String)jTable1.getValueAt(fila, 0).toString());
           String nombre=(String)jTable1.getValueAt(fila, 1);
           String apellido=(String)jTable1.getValueAt(fila, 2);
           String direccion=(String)jTable1.getValueAt(fila, 3);
           String telefono=(String)jTable1.getValueAt(fila, 4);
           
           
           jTextField1.setText(nombre);
            jTextField2.setText(apellido);
           jTextField3.setText(direccion);
           jTextField4.setText(telefono);
            jTextField5.setText(""+id);

          
       }
    }//GEN-LAST:event_jTable1MouseClicked

