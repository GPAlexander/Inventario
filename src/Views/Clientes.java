     private void listaTabla()
        {
           model.addColumn("Id");
           model.addColumn("Nombre");
           model.addColumn("Apellido");
           model.addColumn("Direccion");
           model.addColumn("Telefono");
           jTable1.setModel(model);

        }
      private void GetList()
    {
           model.setRowCount(0);
           List<webservices.Clientes> datos=pac.selectAllClientes(); 
           
           
           model=(DefaultTableModel)jTable1.getModel();
           
 
           Object[]ClientesObj = new Object[5];
           for(webservices.Clientes u:datos)
            {
               
                ClientesObj[0]=u.getId();
                ClientesObj[1]=u.getNombre();
                ClientesObj[2]=u.getApellido();
                ClientesObj[3]=u.getDireccion();
                ClientesObj[4]=u.getTelefono();
                model.addRow(ClientesObj);

                
            }
           jTable1.setModel(model);
        
    }
     
